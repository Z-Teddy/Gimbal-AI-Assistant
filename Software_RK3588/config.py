# ==============================================================================
# 1. 硬件设备配置 (Hardware Configuration)
# ==============================================================================

# 串口设备路径
# Linux/RK3588 环境通常为 '/dev/ttyUSB0' 或 '/dev/ttyS0'
# Windows 环境通常为 'COM3'
SERIAL_PORT = '/dev/ttyUSB0'

# 串口波特率 (需与 STM32 保持一致)
BAUD_RATE = 115200

# 摄像头设备索引
# USB 摄像头通常为 0, RK3588 MIPI 摄像头可能为 11 或其他
CAM_INDEX = 0

# ==============================================================================
# 2. 视觉参数设置 (Computer Vision Settings)
# ==============================================================================

# 摄像头采集分辨率 (Input Resolution)
CAM_WIDTH = 640
CAM_HEIGHT = 480

# 画面翻转设置 (Flip Code)
#  0    : 垂直翻转 (X轴翻转)
#  1    : 水平翻转 (Y轴翻转)
# -1    : 双向翻转 (倒装模式)
# None  : 不翻转
CAM_FLIP = 0 

# ==============================================================================
# 3. 通信协议参数 (Protocol & Logic)
# ==============================================================================

# STM32 端期望的坐标系分辨率 (Target Resolution)
# 用于将视觉坐标 (640x480) 映射到控制坐标 (320x240)
STM32_WIDTH = 319
STM32_HEIGHT = 239

# 串口发送频率限制 (Transmission Interval)
# 单位: 秒. 0.04s = 25Hz
# 作用: 防止发送过快导致下位机中断处理阻塞
SEND_INTERVAL = 0.04